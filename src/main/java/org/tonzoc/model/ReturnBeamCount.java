package org.tonzoc.model;

public class ReturnBeamCount {

    private String name;
    private String leftAndRight;
    private String tenderGuid;
    private String tenderName;
    private String total;
    private String finish;
    private String unFinish;
    private String unSubmit;

    public ReturnBeamCount() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLeftAndRight() {
        return leftAndRight;
    }

    public void setLeftAndRight(String leftAndRight) {
        this.leftAndRight = leftAndRight;
    }

    public String getTenderGuid() {
        return tenderGuid;
    }

    public void setTenderGuid(String tenderGuid) {
        this.tenderGuid = tenderGuid;
    }

    public String getTenderName() {
        return tenderName;
    }

    public void setTenderName(String tenderName) {
        this.tenderName = tenderName;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }

    public String getFinish() {
        return finish;
    }

    public void setFinish(String finish) {
        this.finish = finish;
    }

    public String getUnFinish() {
        return unFinish;
    }

    public void setUnFinish(String unFinish) {
        this.unFinish = unFinish;
    }

    public String getUnSubmit() {
        return unSubmit;
    }

    public void setUnSubmit(String unSubmit) {
        this.unSubmit = unSubmit;
    }
}
